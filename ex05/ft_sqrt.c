#include <unistd.h>

int	ft_sqrt(int nb)
{
	int i;

	if(nb <= 0)
		return (0);
	if(nb == 1)
		return (1);
	i = nb / 2;
	while (!((nb / i) == i && (nb % i) == 0))
	{
		if (i == 1)
			return (0);
		i--;
	}
	return (i);
}
