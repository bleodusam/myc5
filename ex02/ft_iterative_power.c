#include <unistd.h>

int	ft_iterative_power(int nb, int power)
{
	int x;

	if (nb < 0)
		return (0);
	x = 1;
	while (power && nb)
	{
		x = x * nb;
		power = power - 1;
	}
	return (x);
}
